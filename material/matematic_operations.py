# Operações matematicas

# Adição 
#   [valor] + [valor]
# Exemplo a baixo:
print(23+234)

# subtração
#   [valor] - [valor]
# Exemplo a baixo:
print(75-43)

# Multiplicação
#   [valor] * [valor]
# Exemplo a baixo:
print(10*5)

# Divisão
#   [valor] / [valor]
# Exemplo a baixo:
print(12/4)

# Expondencial
# Esse é simples é uma potencia, valor vezes ele mesmo um numero determinado de vezes.
#   [valor] ** [valor]
# Exemplo a baixo:
print(2 ** 3)

# Modulo
# Esse vou explicar porque não são todos que sabem como funciona, então o modulo é o que sobra de uma fração, como no exemplo foi dado a baixo 10 sobre 3 sobra 1, esse é o modulo da fração este restante
#   [valor] % [valor]
# Exemplo a baixo:
print(10 % 3)

